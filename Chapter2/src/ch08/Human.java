package ch08;

public class Human {
	public int height;
	public int weight;
	public int age;
	public String name;
	public boolean sex; // true : 남성, false : 여성
	
	public Human(int height, int weight, int age, String name, boolean sex)
	{
		this.height = height;
		this.weight = weight;
		this.age = age;
		this.name = name;
		this.sex = sex;
	}
}


